    private void atualizarTabela() {

        Object[][] dados = new Object[lista.size()][6];
        int linha = 0;
        for (TesteIMC t : lista) {

            System.out.println(t);

            dados[linha][0] = t.getCodigo();
            dados[linha][1] = t.getNome();
            dados[linha][2] = t.getPeso();
            dados[linha][3] = t.getAltura();
            dados[linha][4] = t.calcularIMC();
            dados[linha][5] = t.interpretarResult();
            linha++;
        }

        jTableLista.setModel(new javax.swing.table.DefaultTableModel(
                dados,
                new String[]{
                    "Codigo", "Nome", "Peso", "Altura", "IMC",
                    "Resultado"
                }
        ));

        limparCampos();
    }

    private void limparCampos() {
        jTextCodigo.setText("");
        jTextNome.setText("");
        jTextPeso.setText("");
        jTextAltura.setText("");
    }
